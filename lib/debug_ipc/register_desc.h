#pragma once

#include <cstdint>

// Holds constant description values for all the register data for all the
// supported architectures.
// The enum definitions mirror the structs defined in the debug information
// for zircon (see zircon/system/public/zircon/syscalls/debug.h).

namespace debug_ipc {

enum class RegisterID : uint32_t {
  kUnknown = 0,

  // ARMv8 (Range: 1000-1999) --------------------------------------------------

  // General purpose

  kARMv8_x0   = 1000,
  kARMv8_x1   = 1001,
  kARMv8_x2   = 1002,
  kARMv8_x3   = 1003,
  kARMv8_x4   = 1004,
  kARMv8_x5   = 1005,
  kARMv8_x6   = 1006,
  kARMv8_x7   = 1007,
  kARMv8_x8   = 1008,
  kARMv8_x9   = 1009,
  kARMv8_x10  = 1010,
  kARMv8_x11  = 1011,
  kARMv8_x12  = 1012,
  kARMv8_x13  = 1013,
  kARMv8_x14  = 1014,
  kARMv8_x15  = 1015,
  kARMv8_x16  = 1016,
  kARMv8_x17  = 1017,
  kARMv8_x18  = 1018,
  kARMv8_x19  = 1019,
  kARMv8_x20  = 1020,
  kARMv8_x21  = 1021,
  kARMv8_x22  = 1022,
  kARMv8_x23  = 1023,
  kARMv8_x24  = 1024,
  kARMv8_x25  = 1025,
  kARMv8_x26  = 1026,
  kARMv8_x27  = 1027,
  kARMv8_x28  = 1028,
  kARMv8_x29  = 1029,
  kARMv8_lr   = 1030,
  kARMv8_sp   = 1031,
  kARMv8_pc   = 1032,
  kARMv8_cpsr = 1034,

  // TODO(donosoc): Add ARMv8 vector registers

  // x64 (Range: 2000-2999) ----------------------------------------------------

  // General purpose

  kX64_rax    = 2000,
  kX64_rbx    = 2001,
  kX64_rcx    = 2002,
  kX64_rdx    = 2003,
  kX64_rsi    = 2004,
  kX64_rdi    = 2005,
  kX64_rbp    = 2006,
  kX64_rsp    = 2007,
  kX64_r8     = 2008,
  kX64_r9     = 2009,
  kX64_r10    = 2010,
  kX64_r11    = 2011,
  kX64_r12    = 2012,
  kX64_r13    = 2013,
  kX64_r14    = 2014,
  kX64_r15    = 2015,
  kX64_rip    = 2016,
  kX64_rflags = 2017,

  // FP (x87 FPU/MMX)

  kX64_fcw = 2100,  // Control word.
  kX64_fsw = 2101,  // Status word.
  kX64_ftw = 2102,  // Tag word.
                    // 2103 reserved
  kX64_fop = 2104,  // Opcode.
  kX64_fip = 2105,  // Instruction pointer.
  kX64_fdp = 2106,  // Data pointer.

  // The x87/MMX state. For x87 the each "st" entry has the low 80 bits used for
  // the register contents. For MMX, the low 64 bits are used.
  // The higher bits are unused.
  kX64_st0 = 2110,
  kX64_st1 = 2111,
  kX64_st2 = 2112,
  kX64_st3 = 2113,
  kX64_st4 = 2114,
  kX64_st5 = 2115,
  kX64_st6 = 2116,
  kX64_st7 = 2117,

  // TODO(donosoc): Add x64 vector registers
};

}   // namespace debug_ipc
